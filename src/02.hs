part1 :: [Int] -> Int
part1 l = maximum l - minimum l

part2 :: [Int] -> Int
part2 l = sum [x `div` y | (x:ys) <- tails l, y <- ys, x `mod` y == 0]

solveParts :: Int -> (Int,Int)
solveParts _ = (sum $ map part1 rows, sum $ map part2 rows)
