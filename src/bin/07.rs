fn part_one() -> i64 {
  [0,1,2,3,4].iter()
    .permutations(5)
    .map(|digits| digits.iter()
      .fold(0, |prev, &&i| {
        let mut cpu = IntCoder::new(&PROGRAM);
        cpu.push_input(i);
        cpu.push_input(prev);
        cpu.execute_until_output()
      })
    )
    .max()
    .unwrap()
}

fn part_two() -> i64 {
  [5,6,7,8,9].iter()
    .permutations(5)
    .map(|digits| {
      let mut cpus = vec![IntCoder::new(&PROGRAM); 5];
      for i in 0..5 { cpus[i].push_input(*digits[i]); }
      let mut input = 0;
      for i in (0..5).cycle() {
        cpus[i].push_input(input);
        match cpus[i].execute() {
          ExitCode::Output(o)  => input = o,
          ExitCode::Halted     => return input,
          ExitCode::AwaitInput => unreachable!(),
        }
      }
      unreachable!()
    })
    .max()
    .unwrap()
}

fn main() {
  let now = Instant::now();
  println!("Part one: {}", part_one());
  println!("Part two: {}", part_two());
  println!("Time: {}ms", now.elapsed().as_millis());
}
